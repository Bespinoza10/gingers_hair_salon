							<p>
  							<strong>1. Choose balayage over foil.</strong> The way your highlights are applied can make a huge difference in how elegantly they grow out. Instead, opt for balayage — the painting technique creates a more natural look that will grow out in a less noticeable way. If your colorist only does foil, ask him to shadow or soften your color at the root to erase any harsh lines that will show up when your color starts to grow out.</p>

<p><strong>2. Ask for ombre.</strong> Carrie McCard, stylist at Rita Hazan, said this style of gradual lightness is the best option for those who want low-maintenance highlights. “It keeps your lightness underneath and you won’t get a demarcation line ( look like you have roots) when your highlights start to grow out.” Not into the big color gradient some ombre styles are known for? Ask for sombre (subtle ombre), and explain to your colorist that you like the ombre look ,but don’t want it to be so dramatic.</p>

<p><strong>3. Or try “babylights.”</strong> The latest nuanced highlight trend, babylights, focuses on super natural lightening, a la the natural highlights you got as a kid from playing outside in the sun. Your colorist can focus on brightening up your hair right in front, and put subtle sun-kissed highlights throughout that will look completely natural as they grow out.</p>

<p><strong>4. Only go a shade or two lighter.</strong> “The lighter you go near the root, the sooner you’ll see regrowth,” notes Angarita. Stick to just highlighting your natural color—ask your colorist to just lighten and brighten your color naturally, rather than doing something drastic. Subtler highlights can look just as striking, and they are way easier to maintain.</p>

<p><strong>5. Skip the shampoo. </strong>Washing your hair less will help your color stay fresh for longer. If you normally shampoo every day, try using dry shampoo every other day instead to cut back. McCard recommends Oribe Dry Texturizing Spray: “It’s a great way to absorb your natural oils and keep your hair smelling great.” When you do wash, make sure to use color-safe shampoo and conditioner</p>

<p><strong>6. Show your hair some extra TLC.</strong> “Good color starts with healthy hair,” says celebrity hairstylist Rita Hazan. Between shampooing, blow-drying, curling, and straightening, we put our hair through a lot on a daily basis. “You have to add nutition back into the hair,” says Hazan. She instructs her clients to use a gloss in-between shampooing and conditioning. Gloss, like Hazan’s Ultimate Shine Color Gloss, repairs the hair cuticle while also adding shine and adjusting tone—so not only will it boost your hair’s health, but it’ll also actually keep your color looking fresh. Also, use a deep conditioner once or twice a week to deeply repair and hydrate hair. We really love Hazan’s two-step Weekly Remedy deep conditioning treatment.</p>

<p><strong>7. Style smartly.</strong> Way overdue for a touchup? A few styling tricks can help you skate by until you can make it into the salon. Change your part. “Change where you part your hair,” McCard suggests. “Zigzag it. Make an extreme side part. Change sides.” Fake it until you make it.
